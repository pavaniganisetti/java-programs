public class PostAndPrefixOperations
{
	public static void main(String[] args) {
		int a = 10;
		int b = 10;
		System.out.println("a=10, postfix a++ "+" "+a++);
		System.out.println("b=10, prefix ++b "+" "+ ++b);
		System.out.println("Value of a, after opeations:"+a);
		System.out.println("Value of b, after opeations:"+b);
	}
}